		<h1 style="text-align: center;">FAQ</h1>
		<h2 class="margin-bottom">What is Pass2Heaven?</h2>
		<p class="margin-bottom">
			Pass2Heaven is an inheritance protocol for sending information directly to the recipient while
			ensuring the data's integrity and confidentiality with verifiable ownership.
		</p>
		<h2 class="margin-bottom margin-top">What is a key pair?</h2>
		<p class="margin-bottom">
			A key pair is a set of two related cryptographic keys that are used to authenticate and secure
			communications. The two keys in a key pair are referred to as the private key and the public
			key.
		</p>
		<p class="margin-bottom">
			The private key is kept secret and is used to decrypt messages that are encrypted with the
			corresponding public key. The public key, on the other hand, is made available to anyone who
			needs to send an encrypted message to the owner of the key pair.
		</p>

		<h2 class="margin-bottom margin-top">What is encryption?</h2>
		<p class="margin-bottom">
			Encryption is the process of converting plaintext (ordinary, unencrypted data) into ciphertext
			(a coded, encrypted form that is unreadable without a secret key). The purpose of encryption
			is to protect data from unauthorized access or tampering by transforming it into a form that
			is unreadable to anyone who does not have the proper decryption key.
		</p>

		<h2 class="margin-bottom margin-top">How does Pass2Heaven protect my data?</h2>
		<p class="margin-bottom">
			In addition to encrypting the data in the inheritance, Pass2Heaven uses Stealth addresses to
			provide the anonymity of the recipient, making it impossible to know who receives what. To
			ensure the irreversibility and integrity of the system history, events are stored and
			timestamped in a blockchain network.
		</p>

		<h2 class="margin-bottom margin-top">Can Pass2Heaven see or delete my data?</h2>
		<p class="margin-bottom">
			Since the data is encrypted with a private key that sender generates on the spot, Pass2Heaven
			can never see or modify the contents of the will. All wills are stored in a public IPFS
			network, which makes deleting them a nearly impossible task for anyone
		</p>

		<h2 class="margin-bottom margin-top">Can I delay sending the inheritance?</h2>
		<p class="margin-bottom">
			At the moment, the only way to delay an inheritance is to give a receiver the key later.
			However, in the near future we plan to add timed inheritances so that you don't have to worry
			about the recipient getting the will earlier than necessary.
		</p>
